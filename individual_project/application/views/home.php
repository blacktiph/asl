		<h1>Blacktip Challenge Shark Fishing Tournament</h1>
		<p>The Blacktip Challenge is a catch, tag and release shark fishing tournament on Florida&#39;s East Coast. We are a conservation based tournament and encourage the release of sharks. The Blacktip Challenge works in a joint research effort with the National Oceanic and Atmospheric Administration (NOAA), the Cooperative Shark Tagging Program (NOAA), and the International Land-Based Shark Fishing Association (ILSFA).</p>
		
		<h2>2014 Tournament</h2>
		<p>The 2014 tournament will take place between Wednesday January 29th and Sunday February 2nd. The anglers meeting will take place on Wednesday January 29th. The awards ceremony will take place on Sunday February 2nd. The time and location of both the anglers meeting and the awards ceremony will be announced in the upcoming months.</p>
		
