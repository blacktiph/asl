	<h1>How to Measure a Shark</h1>
	
	<p>Measuring tapes are included in entry fees, and will be provided for all participating anglers by the tournament staff, along with data cards to record measurements and other information.</p>
	
	<p>To qualify for points, three measurements must be taken on all fish caught in the tournament, and the total length measurement must be at least 5 feet or 60 inches. This data will also be used in a formula to calculate an estimated weight.</p>
	
	<p>It is recommended to record each measurement as it&#39;s taken to avoid errors. Having a teammate help in this instance is beneficial. A thick pencil and a small sized pvc dive slate or similar writing surface is helpful in wet conditions. The required measurements are taken from the points noted below&#58;</p>
	
	<img src="../images/shark_measure.jpg" alt="Diagram of Shark Measurements" />
	<p class="imgtxt">Measurement Points.</p>
	
	<h2>Total Length</h2>
	<p>Measure the total length in inches from the tip of the snout to the tip of the upper lobe of the tail &#40;or caudal&#41; fin. Tape should be laid flat over the entire length of the fish&#39;s back just along the dorsal line and extended to the upper lobe of the tail &#40;or caudal&#41; fin.</p>
	
	<img src="../images/overall.jpg" alt="Overall length measurement" width="500" height="349" />
	<p class="imgtxt">Measuring the total length first, allows you to easily work down to the fork, then girth.</p>
	
	<h2>Fork Length</h2>
	<p>Measure the fork length in inches from the tip of the snout to the fork of the tail. Tape should be laid flat over the entire length of the fish's back just along the dorsal line and extended to the rear of the fork of the tail.</p>
	
	<img src="../images/fork.jpg" alt="Fork length measurement" width="500" height="381" />
	<p class="imgtxt">The fork, measurement will be used with the girth to calculate an estimated weight.</p>
	
	<h2>Girth</h2>
	<p>This is a defining measurement in calculating an estimated weight. Measure the largest diameter or thickest part of the fish in inches. The girth is just behind the pectoral fins extending around the shark in a straight line. The quickest and safest way to take this measurement is to keep your body near the midpoint of the sharks body. This keeps anglers and others away from the strike zone of a shark&#39;s mouth and/or tail. Place the tape under the fish with one hand while gently rolling the fish away from you with the other hand. Then simply walk around to the other side, gently roll the fish in the opposite direction to expose the end of your measuring tape. Take your girth measurement.</p>

	<img src="../images/girth.jpg" alt="Girth measurement" width="500" height="384" />
	<p class="imgtxt">And finally, the girth. Working with others to assist is always recommended.</p>

