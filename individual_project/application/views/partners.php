			<p>The International Land-Based Shark Fishing Association (ILSFA) was formed to unify the sport of land-based shark fishing and its anglers by establishing standards for the compilation and recognition of world records, while also promoting, through research, education and practice, responsible enjoyment and stewardship of marine and coastal resources.</p>
