			<h2>Apex Predators Program</h2>
			<p>The mission of the API is to conduct life history studies of commercially and recreationally important shark species. Our research is focused on distribution and migration patterns, age and growth, reproductive biology, and feeding ecology. We conduct fishery independent surveys of large and small coastal sharks in US waters from Florida to Delaware. Biological samples and catch data are collected at recreational fishing tournaments in the Northeast US. We administer an extensive Cooperative Shark Tagging Program (CSTP) in the Atlantic Ocean, Gulf of Mexico, and Mediterranean Sea, utilizing thousands of volunteer anglers.</p>
			<p><b>Website:</b> <a href="http://na.nefsc.noaa.gov/sharks/">http://na.nefsc.noaa.gov/sharks/</a></p>
